<template>
  <div class="container mx-auto py-4 px-2">
    <div @click="goBack" class="flex gap-4 mt-8">
      <img src="@/assets/images/icons/back-arrow.svg" alt="" />
      <h1 class="md:text-3xl font-medium">Shopping Cart</h1>
    </div>
    <div class="md:flex gap-8">
      <div v-if="itemsCount" class="md:w-3/4">
        <div
          v-for="item in cart"
          :key="item.product.id"
          class="md:flex gap-8 border-b p-4 mt-4"
        >
          <div class="w-2/4">
            <div>
              <div class="flex gap-4">
                <div class="w-24 h-24 overflow-hidden">
                  <img :src="item.product.image" :alt="item.product.title" />
                </div>
                <div class="flex flex-col">
                  <div>
                    <h3 class="text-base font-light">
                      {{ item.product.title }}
                    </h3>
                  </div>
                  <div class="text-sm font-light">size:small</div>
                  <div class="text-sm font-light">color:Brown</div>
                  <div class="flex align-center">
                    <app-button
                      @click="
                        removeProductFromCart({
                          productId: item.product.id,
                          remove: true,
                        })
                      "
                      size="small"
                      >Delete</app-button
                    >
                    <app-button size="small" class="pl-2"
                      >Add to wishlist</app-button
                    >
                  </div>
                </div>
              </div>
            </div>
          </div>
          <div class="md:w-1/4">
            <div class="flex justify-center items-center gap-4">
              <app-button
                @click="removeProductFromCart({ productId: item.product.id })"
                size="small"
                >-
              </app-button>
              <div
                class="
                  text-center
                  font-semibold
                  text-md text-gray-400
                  flex
                  items-center
                "
              >
                {{ item.quantity }}
              </div>
              <app-button @click="addProductToCart(item.product)" size="small">
                +
              </app-button>
            </div>
          </div>
          <div class="md:w-1/4">
            <div class="flex">
              <span class="text-base font-light">{{ item.product.price }}</span>
            </div>
          </div>
        </div>

        <div class="mt-12 flex justify-end gap-2 font-medium text-xl">
          <h1>
            <span class="">{{ `Subtotal (${itemsCount} items):` }}</span>
          </h1>
          <span class="">{{ (parseFloat(totalPrice) * 100) / 100 }} NGN</span>
        </div>
      </div>

      <div v-else class="flex justify-center w-full">
        <h4 class="text-center text-secondary-500 text-xl font-medium">
          No item in your cart
        </h4>
      </div>

      <div v-show="itemsCount" class="mt-4 lg:mt-0 md:w-1/4 bg-primary-100 p-6">
        <p class="font-medium">{{ itemsCount }}</p>
        <p class="flex justify-end font-medium">
          {{ (parseFloat(totalPrice) * 100) / 100 }}
        </p>

        <app-button class="mt-6" color="primary" variant="contained" fullWidth
          >Proceed to checkout</app-button
        >
        <ContentsAddToCart />
      </div>
    </div>
  </div>
</template>

<script>
import { mapGetters, mapActions } from "vuex";
import AppButton from "@/components/buttons/Button.vue";

export default {
  components: {
    "app-button": AppButton,
  },
  computed: {
    ...mapGetters({
      cart: "cart/cartProducts",
      totalPrice: "cart/cartTotalPrice",
      itemsCount: "cart/cartItemsCount",
    }),
  },
  mounted() {
    const cart = window.localStorage.getItem("ys-cart");
    this.$store.commit("cart/setCart", cart);
  },
  methods: {
    ...mapActions("cart", ["addProductToCart", "removeProductFromCart"]),
    goBack() {
      this.$router.go(-1);
    },
  }
} 
